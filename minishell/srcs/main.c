#include "minishell.h"

int	main(int ac, char **av, char **envp)
{
	t_shell	*shell;

	shell = new_shell(envp);
	if (!shell)
		built_in_exit(NULL);
	run_shell(&shell);
	(void)ac;
	(void)av;
	return (0);
}
